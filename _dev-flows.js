function renderDevFlow(d) {
  const esc = (v) => String(v ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');

  const list = (items, tag = 'ul') => {
    if (!Array.isArray(items) || items.length === 0) {
      return '<p class="empty">Not specified.</p>';
    }
    const itemHtml = items.map((x) => `<li>${esc(x)}</li>`).join('');
    return `<${tag}>${itemHtml}</${tag}>`;
  };

  const section = (title, body) => `<div class="df-block"><h5>${esc(title)}</h5>${body}</div>`;

  const pieces = [
    section('What it is', `<p>${esc(d.what || '')}</p>`),
    section('What it is for', `<p>${esc(d.for || '')}</p>`),
    section('Prerequisites', list(d.prereq, 'ul')),
    section('Data models', list(d.models, 'ul')),
    section('Integrations', list(d.integrations, 'ul')),
    section('API endpoints', list(d.apis, 'ul')),
    section('UI components', list(d.ui, 'ul')),
    section('Development steps', list(d.steps, 'ol')),
    section('Validations', list(d.validate, 'ul')),
    section('Testing checklist', list(d.test, 'ul'))
  ];

  if (Array.isArray(d.ch) && d.ch.length > 0) {
    pieces.push(section('Companies House forms', list(d.ch, 'ul')));
  }

  return `<details class="dev-flow"><summary><i class="fas fa-code"></i> Developer Implementation Guide</summary><div class="dev-flow-body">${pieces.join('')}</div></details>`;
}

